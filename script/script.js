function csvJSON(csv){

    var lines=csv.split("\n");
  
    var result = [];
  
    // NOTE: If your columns contain commas in their values, you'll need
    // to deal with those before doing the next step 
    // (you might convert them to &&& or something, then covert them back later)
    // jsfiddle showing the issue https://jsfiddle.net/
    var headers=lines[0].split(",");
  
    for(var i=1;i<lines.length;i++){
  
        var obj = {};
        var currentline=lines[i].split(",");
  
        for(var j=0;j<headers.length;j++){
            obj[headers[j]] = currentline[j];
        }
  
        result.push(obj);
  
    }
  
    //return result; //JavaScript object
    return result; //JSON
  }



  $(document).ready(function () {
    var gamesArray = [];
    var loadedGames = 0;
    var gamesPerLoad = 30;
    
    function parseGamesData() {
        var lines = games.split('\n');
        lines.shift(); // Remove header
        lines.forEach(line => {
            var parts = line.split(',');
            if (parts.length === 3) {
                gamesArray.push({
                    name: parts[0].trim(),
                    link: parts[1].trim(),
                    image: parts[2].trim()
                });
            }
        });
    }

    function displayGames(filteredGames) {
        $('.grid-container').empty();
        var gamesToDisplay = filteredGames.slice(0, loadedGames + gamesPerLoad);
        
        if (gamesToDisplay.length === 0) {
            $('.grid-container').append('<p class="no-results">No games match your search.</p>');
        } else {
            gamesToDisplay.forEach(game => {
                var gameItem = `<div class="grid-item">
                                    <a href="${game.link}" target="_blank">
                                        <img src="${game.image}" alt="${game.name}">
                                        <p id="item-name">${game.name}</p>
                                    </a>
                                </div>`;
                $('.grid-container').append(gameItem);
            });
        }
    }
    
    function searchGrid() {
        var searchValue = $('#search').val().toLowerCase();
        var filteredGames = gamesArray.filter(game => game.name.toLowerCase().includes(searchValue));
        loadedGames = 0;
        displayGames(filteredGames);
        
        if (searchValue) {
            $('.loadmore-btn').hide();
        } else {
            $('.loadmore-btn').show();
        }
    }
    
    function LoadNext() {
        loadedGames += gamesPerLoad;
        displayGames(gamesArray);
    }

    parseGamesData();
    displayGames(gamesArray);

    $('#search').on('keyup', searchGrid);
    $('.loadmore-btn').on('click', LoadNext);
});

// Theme switcher
function toggleTheme() {
    const body = document.body;
    const themeSwitcher = document.getElementById('theme-switcher');
    const moon = document.getElementById('moon');
    const sun = document.getElementById('sun');
    const griditem = document.querySelectorAll('.grid-item');
    const loadmorebtn = document.querySelector('.loadmore-btn');
    const search = document.getElementById('search');
    const alink = document.querySelectorAll('a');
    body.classList.toggle('dark-theme');
    themeSwitcher.classList.toggle('dark-theme');

    if (body.classList.contains('dark-theme')) {
        alink.forEach(link => {
            link.style.color = '#fff';
        });
        body.style.backgroundColor = '#333';
        body.style.color = '#fff';
        griditem.forEach(item => {
            item.style.backgroundColor = '#444';
        });
        griditem.forEach(item => {
            const p = item.querySelector('p');
            if (p) {
                p.style.color = '#fff';
            }
        });
        moon.style.transform = 'translate(0, 0)';
        sun.style.transform = 'translate(0, 0)';
        loadmorebtn.style.backgroundColor = '#444';
        loadmorebtn.style.color = '#fff';
        search.style.backgroundColor = '#444';
        search.style.color = '#fff';
        search.style.boxShadow = '0 4px 8px rgba(255, 255, 255, 0.1)';
    } else {
        alink.forEach(link => {
            link.style.color = '#333';
        });
        body.style.backgroundColor = '#f4f4f4';
        body.style.color = '#333';
        griditem.forEach(item => {
            item.style.backgroundColor = '#fff';
        });
        griditem.forEach(item => {
            const p = item.querySelector('p');
            if (p) {
                p.style.color = '#333';
            }
        });
        moon.style.transform = 'translate(20px, 20px)';
        sun.style.transform = 'translate(20px, 20px)';
        loadmorebtn.style.backgroundColor = '#fff';
        loadmorebtn.style.color = '#333';
        search.style.backgroundColor = '#fff';
        search.style.color = '#333';
        search.style.boxShadow = '0 4px 8px rgba(0, 0, 0, 0.1)';
    }
}